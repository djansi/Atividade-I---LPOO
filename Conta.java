package estudo1;

public class Conta {
	
	long numero;
	String titular;
	String tipoconta;
	float saldo;
	
	
	public void inserirnumero ( long numero) {
		this.numero = numero;
	}
	
	public void inserirtitular ( String titular) {
		this.titular = titular;
	}
	
		
	public void inserirtipoconta ( String tipoconta) {
		this.tipoconta = tipoconta;
	}
	
	public void inserirsaldo ( float saldo) {
		this.saldo = saldo;
	}
		

}
